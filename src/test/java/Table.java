public class Table {
    //---------ATTRIBUTES-------

    private double weight;
    private double HeightOfLegs;
    private double tableTopHeight;
    private double widthOfLegs;
    private double tableTooWidth;

    //---------GETTERS/SETTERS----------

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getHeightOfLegs() {
        return HeightOfLegs;
    }

    public void setHeightOfLegs(double heightOfLegs) {
        HeightOfLegs = heightOfLegs;
    }

    public double getTableTopHeight() {
        return tableTopHeight;
    }

    public void setTableTopHeight(double tableTopHeight) {
        this.tableTopHeight = tableTopHeight;
    }

    public double getWidthOfLegs() {
        return widthOfLegs;
    }

    public void setWidthOfLegs(double widthOfLegs) {
        this.widthOfLegs = widthOfLegs;
    }

    public double getTableTooWidth() {
        return tableTooWidth;
    }

    public void setTableTooWidth(double tableTooWidth) {
        this.tableTooWidth = tableTooWidth;
    }
}
