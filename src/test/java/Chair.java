public class Chair {
    //---------ATTRIBUTES----------

    private double heightOfLegs;
    private double backrestHeight;
    private double height;
    private double width;
    private double weight;

    //-----GETTERS/SETTERS---------

    public double getHeightOfLegs() {
        return heightOfLegs;
    }

    public void setHeightOfLegs(double heightOfLegs) {
        this.heightOfLegs = heightOfLegs;
    }

    public double getBackrestHeight() {
        return backrestHeight;
    }

    public void setBackrestHeight(double backrestHeight) {
        this.backrestHeight = backrestHeight;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }
}
