public class Knife {
    //---------ATTRIBUTES----------

    private double handleLength;
    private double bladeLength;
    private double weight;
    private double bladeAngle;
    private double widthHandle;

    //---------GETTERS/SETTERS--------

    public double getHandleLength() {
        return handleLength;
    }

    public void setHandleLength(double handleLength) {
        this.handleLength = handleLength;
    }

    public double getBladeLength() {
        return bladeLength;
    }

    public void setBladeLength(double bladeLength) {
        this.bladeLength = bladeLength;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getBladeAngle() {
        return bladeAngle;
    }

    public void setBladeAngle(double bladeAngle) {
        this.bladeAngle = bladeAngle;
    }

    public double getWidthHandle() {
        return widthHandle;
    }

    public void setWidthHandle(double widthHandle) {
        this.widthHandle = widthHandle;
    }
}
