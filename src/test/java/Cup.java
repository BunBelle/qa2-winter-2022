public class Cup {
    //---------ATTRIBUTE----------

    private double weight;
    private double width;
    private double height;
    private double depth;
    private double handleLenght;

    //---------GETTERS/SETTERS---------

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getDepth() {
        return depth;
    }

    public void setDepth(double depth) {
        this.depth = depth;
    }

    public double getHandleLenght() {
        return handleLenght;
    }

    public void setHandleLenght(double handleLenght) {
        this.handleLenght = handleLenght;
    }
}
