public class Sponge {
    //-------ATTRIBUTES--------

    private double weight;
    private double width;
    private double height;
    private double degreeOfAngle;
    private double  weightWithWater;

    //-------GETTERS/SETTERS---------

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getDegreeOfAngle() {
        return degreeOfAngle;
    }

    public void setDegreeOfAngle(double degreeOfAngle) {
        this.degreeOfAngle = degreeOfAngle;
    }

    public double getWeightWithWater() {
        return weightWithWater;
    }

    public void setWeightWithWater(double weightWithWater) {
        this.weightWithWater = weightWithWater;
    }
}
